                  ></span>
                  <p style="margin-top: 5px">
                    还没有账户？
                    <router-link to="/regist" class="">注册</router-link>
                  </p>
                </div>
                <div class="login">
                  <a @click="qqLogin"> <img src="../../assets/QQ.svg" /></a>
                  <a
                    href="https://api.weibo.com/oauth2/authorize?client_id=3751771182&response_type=code&redirect_uri=http://xiyanit.cn/xiyan/system/weibo/back"
                  >
                    <img src="../../assets/weibo.svg"
                  /></a>
                </div>
              </div>
            </div>
          </div>
        </div>
      </Col>
    </Row>
  </div>
</template>

<script>
// import "vue-nav-tabs/themes/paper.css";
// mixin
import { qqLogin } from "@/api/login";
import { getAes } from "@/utils/auth";
import { AESEncrypt } from "@/api/aes";
export default {
  name: "registForm",
  props: {
    userNameRules: {
      type: Array,
      default: () => {
        return [{ required: true, message: "账号不能为空", trigger: "blur" }];
      },
    },
    passwordRules: {
      type: Array,
      default: () => {
        return [{ required: true, message: "密码不能为空", trigger: "blur" }];
      },
    },
  },
  inject: ["reload"],
  data() {
    return {
      //加密后请求服务器的参数
      res: {
        requestData: "",
      },
      form: {
        username: "",
        password: "",
      },
    };
  },
  created() {
    // this.getBook()
  },
  computed: {
    rules() {
      return {
        username: this.userNameRules,
        password: this.passwordRules,
      };
    },
  },
  methods: {
    logins() {
      // alert("请注意去你的邮箱查收验证码哦");
      if (this.form.username === null || this.form.username === "") {
        this.$Message.warning("请输入你的邮箱哦！");
        return;
      }
      if (this.form.password === null || this.form.password === "") {
        this.$Message.warning("请输入你的密码哦！");
        return;
      }
      //获取保存在cookie的AES密钥
      let aesKey = getAes();
      //进行参数加密,必须把对象转换json字符串，不然加密不了
      let dataJson = JSON.stringify(this.form);
      //数据进行加密
      this.res.requestData = AESEncrypt(dataJson, aesKey);
      this.$store.dispatch("Login", this.res).then(() => {
        this.$router.push({ name: "home" });
        this.reload();
      });
    },

    handleSubmit() {
      this.$refs.registForm.validate((valid) => {
        if (valid) {
          this.$emit("on-success-valid", {
            username: this.form.username,
            password: this.form.password,
          });
        }
      });
    },
    qqLogin() {
      qqLogin().then((res) => {
        location.href = res.url;
      });
    },
  },
};
</script>
